// Retry scanner with delay for lazy-loaded forms
async function scanWithRetry(tabId, maxRetries = 3, delayMs = 1000) {
  for (let attempt = 0; attempt < maxRetries; attempt++) {
    // Inject scanner into main page
    const mainResults = await chrome.scripting.executeScript({
      target: { tabId: tabId },
      func: scanFormFields
    });

    let fields = mainResults?.[0]?.result || [];

    // Also try to scan all frames
    try {
      const frameResults = await chrome.scripting.executeScript({
        target: { tabId: tabId, allFrames: true },
        func: scanFormFields
      });

      // Merge results from all frames
      if (frameResults && frameResults.length > 1) {
        frameResults.forEach((result, idx) => {
          if (idx > 0 && result.result && result.result.length > 0) {
            // Mark fields with their frame index
            const frameFields = result.result.map(f => ({
              ...f,
              frameIndex: idx,
              iframeIndex: idx
            }));
            fields = fields.concat(frameFields);
          }
        });
      }
    } catch (e) {
      // Some frames might be inaccessible (cross-origin)
      console.log('Could not scan some frames:', e.message);
    }

    if (fields.length > 0) {
      return fields;
    }

    // Wait before retrying
    if (attempt < maxRetries - 1) {
      await new Promise(resolve => setTimeout(resolve, delayMs));
    }
  }

  return [];
}

async function handleSmartFill() {
  const select = document.getElementById('candidateSelect');
  if (!select.value) {
    showStatus('fillStatus', 'Please select a candidate first', 'error');
    return;
  }

  if (!settings.openaiKey) {
    showStatus('fillStatus', 'Please configure OpenAI API key in settings', 'warning');
    document.getElementById('settingsModal').classList.add('show');
    return;
  }

  const candidate = candidates[parseInt(select.value)];
  const btn = document.getElementById('smartFillBtn');
  btn.disabled = true;
  btn.innerHTML = '<div class="spinner spinner-small"></div> Scanning...';

  try {
    // Get the current tab
    const [tab] = await chrome.tabs.query({ active: true, currentWindow: true });

    // Scan with retry for lazy-loaded forms
    const fields = await scanWithRetry(tab.id, 3, 1000);
    detectedQuestions = fields;

    if (fields.length === 0) {
      showStatus('fillStatus', 'No form fields detected. Try scrolling to load the form, then retry.', 'warning');
      return;
    }

    // Show detected questions
    renderDetectedQuestions(fields);

    // Process each field
    btn.innerHTML = '<div class="spinner spinner-small"></div> Filling...';

    let filled = 0;
    let fileUploads = 0;
    for (const field of fields) {
      const answer = await getAnswerForField(field, candidate);
      if (answer) {
        // Skip file uploads (can't auto-fill)
        if (answer === '__FILE_UPLOAD__' || field.fieldType === 'file') {
          fileUploads++;
          updateQuestionStatus(field.selector, 'pending');
          continue;
        }

        // Fill the field with type info (ensure all args are serializable)
        try {
          const result = await chrome.scripting.executeScript({
            target: { tabId: tab.id, allFrames: true },
            func: fillSingleField,
            args: [
              field.selector,
              answer,
              field.fieldType || 'text',
              field.radioGroup || null,
              field.iframeIndex !== undefined ? field.iframeIndex : null,
              field.shadowHost || null
            ]
          });

          // Check if any frame successfully filled the field
          const success = result && result.some(r => r.result === true);
          if (success) {
            filled++;
            updateQuestionStatus(field.selector, 'filled');
          } else {
            console.log('Failed to fill:', field.label, field.selector);
          }
        } catch (fillError) {
          console.error('Error filling field:', field.label, fillError);
        }
      }
    }

    let statusMsg = `Filled ${filled} of ${fields.length} fields!`;
    if (fileUploads > 0) {
      statusMsg += ` (${fileUploads} file upload${fileUploads > 1 ? 's' : ''} need manual action)`;
    }
    showStatus('fillStatus', statusMsg, 'success');

  } catch (error) {
    showStatus('fillStatus', 'Error: ' + error.message, 'error');
  } finally {
    btn.disabled = false;
    btn.innerHTML = `
      <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">
        <path d="M12 2L2 7l10 5 10-5-10-5z"/><path d="M2 17l10 5 10-5"/><path d="M2 12l10 5 10-5"/>
      </svg>
      Smart Fill with AI
    `;
  }
}

