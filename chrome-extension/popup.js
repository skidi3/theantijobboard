async function handleSmartFill() {
  const select = document.getElementById('candidateSelect');
  if (!select.value) {
    showStatus('fillStatus', 'Please select a candidate first', 'error');
    return;
  }

  if (!settings.openaiKey) {
    showStatus('fillStatus', 'Please configure OpenAI API key in settings', 'warning');
    document.getElementById('settingsModal').classList.add('show');
    return;
  }

  const candidate = candidates[parseInt(select.value)];
  const btn = document.getElementById('smartFillBtn');
  btn.disabled = true;
  btn.innerHTML = '<div class="spinner spinner-small"></div> Scanning...';

  try {
    // Get the current tab
    const [tab] = await chrome.tabs.query({ active: true, currentWindow: true });

    // Inject the scanner and get form fields
    const results = await chrome.scripting.executeScript({
      target: { tabId: tab.id },
      func: scanFormFields
    });

    if (!results || !results[0]) {
      throw new Error('Could not scan the page');
    }

    const fields = results[0].result;
    detectedQuestions = fields;

    if (fields.length === 0) {
      showStatus('fillStatus', 'No form fields detected on this page', 'warning');
      return;
    }

    // Show detected questions
    renderDetectedQuestions(fields);

    // Process each field
    btn.innerHTML = '<div class="spinner spinner-small"></div> Filling...';

    let filled = 0;
    let fileUploads = 0;
    for (const field of fields) {
      const answer = await getAnswerForField(field, candidate);
      if (answer) {
        // Skip file uploads (can't auto-fill)
        if (answer === '__FILE_UPLOAD__' || field.fieldType === 'file') {
          fileUploads++;
          updateQuestionStatus(field.selector, 'pending');
          continue;
        }

        // Fill the field with type info (ensure all args are serializable)
        const result = await chrome.scripting.executeScript({
          target: { tabId: tab.id },
          func: fillSingleField,
          args: [field.selector, answer, field.fieldType || 'text', field.radioGroup || null]
        });

        if (result && result[0] && result[0].result) {
          filled++;
          updateQuestionStatus(field.selector, 'filled');
        }
      }
    }

    let statusMsg = `Filled ${filled} of ${fields.length} fields!`;
    if (fileUploads > 0) {
      statusMsg += ` (${fileUploads} file upload${fileUploads > 1 ? 's' : ''} need manual action)`;
    }
    showStatus('fillStatus', statusMsg, 'success');

  } catch (error) {
    showStatus('fillStatus', 'Error: ' + error.message, 'error');
  } finally {
    btn.disabled = false;
    btn.innerHTML = `
      <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">
        <path d="M12 2L2 7l10 5 10-5-10-5z"/><path d="M2 17l10 5 10-5"/><path d="M2 12l10 5 10-5"/>
      </svg>
      Smart Fill with AI
    `;
  }
}

