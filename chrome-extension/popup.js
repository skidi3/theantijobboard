// ==================== FORM SCANNING FUNCTION (INJECTED) ====================
function scanFormFields() {
  const fields = [];

  // Scan ALL form elements
  const inputs = document.querySelectorAll('input, textarea, select');

  // Common patterns for different field types
  const basicPatterns = {
    firstName: /first.?name|given.?name|fname/i,
    lastName: /last.?name|family.?name|surname|lname/i,
    fullName: /full.?name|^name$|your.?name|candidate.?name/i,
    email: /e?.?mail/i,
    phone: /phone|mobile|tel|cell/i,
    phoneCode: /country.?code|phone.?code|dial.?code|calling.?code/i,
    linkedin: /linkedin/i,
    github: /github/i,
    portfolio: /portfolio|website|personal.?site|url/i,
    location: /location|city|address|where.?are.?you/i,
    country: /country/i,
    state: /state|province|region/i,
    zipCode: /zip|postal|postcode/i,
    title: /title|role|position|current.?title|job.?title/i,
    company: /company|employer|current.?company|organization/i,
    years: /years|experience|yoe/i,
    skills: /skills|technologies|tech.?stack/i,
    workAuth: /authorized|work.?auth|visa|legally|eligible.?to.?work|sponsorship/i,
    remote: /remote|on.?site|hybrid|work.?location|work.?preference/i,
    startDate: /start.?date|available|when.?can.?you|earliest/i,
    salary: /salary|compensation|pay|expected.?ctc/i,
    gender: /gender|sex/i,
    ethnicity: /ethnicity|race|ethnic/i,
    veteran: /veteran|military/i,
    disability: /disability|disabled/i,
    hearAbout: /hear.?about|how.?did.?you|source|referral/i
  };

  // Patterns that require AI
  const aiPatterns = [
    /why.?you|motivation|interested|what.?draws|why.?do.?you.?want/i,
    /cover.?letter|summary|about|tell.?us.?about/i,
    /describe|explain|elaborate|how.?would/i,
    /strength|weakness|challenge|achievement|accomplishment/i,
    /additional|anything.?else|other.?info/i
  ];

  inputs.forEach((input, index) => {
    // Skip hidden and submit/button
    if (['hidden', 'submit', 'button', 'image', 'reset'].includes(input.type)) return;

    // Get all identifying info
    const name = (input.name || '').toLowerCase();
    const id = (input.id || '').toLowerCase();
    const placeholder = (input.placeholder || '');
    const ariaLabel = (input.getAttribute('aria-label') || '');

    // Get label text
    let labelText = '';
    if (input.id) {
      const label = document.querySelector(`label[for="${input.id}"]`);
      if (label) labelText = label.textContent.trim();
    }
    if (!labelText) {
      const parentLabel = input.closest('label');
      if (parentLabel) labelText = parentLabel.textContent.trim();
    }
    if (!labelText) {
      const labelledBy = input.getAttribute('aria-labelledby');
      if (labelledBy) {
        const labelEl = document.getElementById(labelledBy);
        if (labelEl) labelText = labelEl.textContent.trim();
      }
    }
    if (!labelText) {
      const parent = input.parentElement;
      if (parent) {
        const prev = input.previousElementSibling;
        if (prev && ['LABEL', 'SPAN', 'DIV', 'P'].includes(prev.tagName)) {
          labelText = prev.textContent.trim();
        }
      }
    }
    // Also check for legend in fieldset (common for radio groups)
    if (!labelText) {
      const fieldset = input.closest('fieldset');
      if (fieldset) {
        const legend = fieldset.querySelector('legend');
        if (legend) labelText = legend.textContent.trim();
      }
    }

    const identifiers = [name, id, placeholder.toLowerCase(), labelText.toLowerCase(), ariaLabel.toLowerCase()].join(' ');

    // Determine field type
    let fieldType = 'text';
    let inputType = input.type || 'text';

    if (input.tagName === 'SELECT') {
      fieldType = input.multiple ? 'multiselect' : 'select';
    } else if (input.tagName === 'TEXTAREA') {
      fieldType = 'textarea';
    } else if (inputType === 'checkbox') {
      fieldType = 'checkbox';
    } else if (inputType === 'radio') {
      fieldType = 'radio';
    } else if (inputType === 'file') {
      fieldType = 'file';
    } else {
      fieldType = inputType;
    }

    // Get options for select elements
    let options = [];
    if (input.tagName === 'SELECT') {
      options = Array.from(input.options).map(opt => ({
        value: opt.value,
        text: opt.textContent.trim()
      }));
    }

    // For radio buttons, get all options in the group
    let radioOptions = [];
    if (inputType === 'radio' && input.name) {
      const radios = document.querySelectorAll(`input[type="radio"][name="${input.name}"]`);
      radioOptions = Array.from(radios).map(r => {
        let radioLabel = '';
        if (r.id) {
          const lbl = document.querySelector(`label[for="${r.id}"]`);
          if (lbl) radioLabel = lbl.textContent.trim();
        }
        if (!radioLabel) {
          const parentLbl = r.closest('label');
          if (parentLbl) radioLabel = parentLbl.textContent.trim();
        }
        return {
          value: r.value,
          text: radioLabel || r.value
        };
      });
    }

    // Check if requires AI
    let requiresAI = false;
    let isBasic = false;

    for (const pattern of Object.values(basicPatterns)) {
      if (pattern.test(identifiers)) {
        isBasic = true;
        break;
      }
    }

    if (fieldType === 'textarea') {
      requiresAI = true;
    } else {
      for (const pattern of aiPatterns) {
        if (pattern.test(identifiers)) {
          requiresAI = true;
          break;
        }
      }
    }

    // Skip if already filled (for text inputs)
    if (['text', 'email', 'tel', 'url', 'textarea'].includes(fieldType)) {
      if (input.value && input.value.trim()) return;
    }

    // Skip if checkbox already checked
    if (fieldType === 'checkbox' && input.checked) return;

    // Skip if select already has non-default value
    if (fieldType === 'select' && input.selectedIndex > 0) return;

    // For radio buttons, only add one entry per group
    if (fieldType === 'radio') {
      const groupName = input.name;
      if (fields.some(f => f.radioGroup === groupName)) return;
    }

    // Generate selector
    let selector = '';
    if (input.id) {
      selector = `#${CSS.escape(input.id)}`;
    } else if (input.name) {
      selector = `[name="${CSS.escape(input.name)}"]`;
    } else {
      selector = `${input.tagName.toLowerCase()}:nth-of-type(${index + 1})`;
    }

    fields.push({
      selector,
      label: labelText || placeholder || ariaLabel || name || 'Unknown field',
      fieldType,
      inputType,
      placeholder,
      maxLength: input.maxLength > 0 ? input.maxLength : null,
      requiresAI,
      isBasic,
      options: options.length > 0 ? options : undefined,
      radioOptions: radioOptions.length > 0 ? radioOptions : undefined,
      radioGroup: fieldType === 'radio' ? input.name : undefined,
      accept: fieldType === 'file' ? input.accept : undefined
    });
  });

  return fields;
}

// Fill a single field (injected) - handles all field types
function fillSingleField(selector, value, fieldType, radioGroup) {
  try {
    const element = document.querySelector(selector);
    if (!element) return false;

    switch (fieldType) {
      case 'text':
      case 'email':
      case 'tel':
      case 'url':
      case 'number':
      case 'textarea':
        element.value = value;
        element.dispatchEvent(new Event('input', { bubbles: true }));
        element.dispatchEvent(new Event('change', { bubbles: true }));
        element.dispatchEvent(new Event('blur', { bubbles: true }));
        return true;

      case 'select':
        // Try to find matching option by value or text
        const options = Array.from(element.options);
        let matched = false;

        // First try exact value match
        for (let i = 0; i < options.length; i++) {
          if (options[i].value.toLowerCase() === value.toLowerCase()) {
            element.selectedIndex = i;
            matched = true;
            break;
          }
        }

        // Then try text contains match
        if (!matched) {
          for (let i = 0; i < options.length; i++) {
            if (options[i].textContent.toLowerCase().includes(value.toLowerCase()) ||
                value.toLowerCase().includes(options[i].textContent.toLowerCase())) {
              element.selectedIndex = i;
              matched = true;
              break;
            }
          }
        }

        // For country, try common variations
        if (!matched && /united states|usa|us/i.test(value)) {
          for (let i = 0; i < options.length; i++) {
            const txt = options[i].textContent.toLowerCase();
            if (txt.includes('united states') || txt === 'usa' || txt === 'us' || txt.includes('america')) {
              element.selectedIndex = i;
              matched = true;
              break;
            }
          }
        }

        if (matched) {
          element.dispatchEvent(new Event('change', { bubbles: true }));
          element.dispatchEvent(new Event('blur', { bubbles: true }));
        }
        return matched;

      case 'multiselect':
        // For multi-select, value should be comma-separated
        const valuesToSelect = value.split(',').map(v => v.trim().toLowerCase());
        const multiOptions = Array.from(element.options);

        multiOptions.forEach(opt => {
          const optText = opt.textContent.toLowerCase();
          const optValue = opt.value.toLowerCase();
          if (valuesToSelect.some(v => optText.includes(v) || optValue.includes(v) || v.includes(optText))) {
            opt.selected = true;
          }
        });

        element.dispatchEvent(new Event('change', { bubbles: true }));
        return true;

      case 'checkbox':
        // value should be boolean or 'true'/'false' or 'yes'/'no'
        const shouldCheck = value === true || value === 'true' || value === 'yes' || value === '1';
        if (element.checked !== shouldCheck) {
          element.checked = shouldCheck;
          element.dispatchEvent(new Event('change', { bubbles: true }));
          element.dispatchEvent(new Event('click', { bubbles: true }));
        }
        return true;

      case 'radio':
        // Find the radio button with matching value in the group
        if (radioGroup) {
          const radios = document.querySelectorAll(`input[type="radio"][name="${radioGroup}"]`);
          for (const radio of radios) {
            const radioLabel = radio.closest('label')?.textContent?.toLowerCase() || '';
            const labelFor = document.querySelector(`label[for="${radio.id}"]`)?.textContent?.toLowerCase() || '';
            const radioValue = radio.value.toLowerCase();
            const searchValue = value.toLowerCase();

            if (radioValue === searchValue ||
                radioLabel.includes(searchValue) ||
                labelFor.includes(searchValue) ||
                searchValue.includes(radioValue)) {
              radio.checked = true;
              radio.dispatchEvent(new Event('change', { bubbles: true }));
              radio.dispatchEvent(new Event('click', { bubbles: true }));
              return true;
            }
          }
        }
        return false;

      case 'file':
        // Can't programmatically set file input for security reasons
        // But we can highlight it for manual action
        element.style.outline = '3px solid #f43f5e';
        element.style.outlineOffset = '2px';
        console.log('File upload required:', selector, '- Please upload manually');
        return false;

      default:
        element.value = value;
        element.dispatchEvent(new Event('input', { bubbles: true }));
        element.dispatchEvent(new Event('change', { bubbles: true }));
        return true;
    }
  } catch (e) {
    console.error('Fill error:', e);
  }
  return false;
}

// Basic form filling (injected)
function fillBasicForm(candidate) {
  let filled = 0;

  const patterns = {
    firstName: /first.?name|given.?name|fname/i,
    lastName: /last.?name|family.?name|surname|lname/i,
    fullName: /full.?name|^name$|your.?name|candidate.?name/i,
    email: /e?.?mail/i,
    phone: /phone|mobile|tel|cell/i,
    linkedin: /linkedin/i,
    github: /github/i,
    portfolio: /portfolio|website|personal.?site|url/i,
    location: /location|city|address|where.?are.?you/i,
    title: /title|role|position|current.?title|job.?title/i,
    company: /company|employer|current.?company|organization/i,
    years: /years|experience|yoe/i,
    skills: /skills|technologies|tech.?stack/i,
    summary: /summary|cover|about|tell.?us|why.?you|motivation|intro/i
  };

  const nameParts = candidate.name.split(' ');
  const firstName = nameParts[0] || '';
  const lastName = nameParts.slice(1).join(' ') || '';

  const values = {
    firstName,
    lastName,
    fullName: candidate.name,
    email: candidate.email,
    phone: candidate.phone,
    linkedin: candidate.linkedin,
    github: candidate.github,
    portfolio: candidate.portfolio,
    location: candidate.location,
    title: candidate.title,
    company: candidate.company,
    years: candidate.years,
    skills: candidate.skills,
    summary: candidate.summary
  };

  const inputs = document.querySelectorAll('input, textarea, select');

  inputs.forEach(input => {
    if (['hidden', 'submit', 'button', 'file'].includes(input.type)) return;

    const name = (input.name || '').toLowerCase();
    const id = (input.id || '').toLowerCase();
    const placeholder = (input.placeholder || '').toLowerCase();

    let label = '';
    if (input.id) {
      const labelEl = document.querySelector(`label[for="${input.id}"]`);
      if (labelEl) label = labelEl.textContent.toLowerCase();
    }
    if (!label) {
      const parentLabel = input.closest('label');
      if (parentLabel) label = parentLabel.textContent.toLowerCase();
    }

    const ariaLabel = (input.getAttribute('aria-label') || '').toLowerCase();
    const identifiers = [name, id, placeholder, label, ariaLabel].join(' ');

    for (const [key, pattern] of Object.entries(patterns)) {
      if (pattern.test(identifiers) && values[key]) {
        if (key === 'fullName' && (patterns.firstName.test(identifiers) || patterns.lastName.test(identifiers))) {
          continue;
        }

        if (!input.value || input.value.trim() === '') {
          input.value = values[key];
          input.dispatchEvent(new Event('input', { bubbles: true }));
          input.dispatchEvent(new Event('change', { bubbles: true }));
          filled++;
        }
        break;
      }
    }
  });

  return filled;
}

// ==================== CAPTURE FUNCTIONALITY ====================
function extractInfoFromText(text, url) {
  const result = {
    company: '',
    roles: '',
    category: 'tech',
    why_included: ''
  };

  const companyPatterns = [
    /(?:at|@|join(?:ing)?)\s+([A-Z][A-Za-z0-9]+(?:\s+[A-Z][A-Za-z0-9]+)?)/i,
    /([A-Z][A-Za-z0-9]+(?:\s+[A-Z][A-Za-z0-9]+)?)\s+is\s+hiring/i,
    /hiring\s+(?:at|for)\s+([A-Z][A-Za-z0-9]+)/i,
    /([A-Z][a-z]+(?:[A-Z][a-z]+)+)/
  ];

  for (const pattern of companyPatterns) {
    const match = text.match(pattern);
    if (match && match[1]) {
      result.company = match[1].trim();
      break;
    }
  }

  if (!result.company && url) {
    const twitterMatch = url.match(/twitter\.com\/([^\/]+)|x\.com\/([^\/]+)/);
    if (twitterMatch) {
      result.company = twitterMatch[1] || twitterMatch[2];
    }
  }

  const roleKeywords = [
    'engineer', 'developer', 'designer', 'manager', 'lead', 'director',
    'analyst', 'scientist', 'architect', 'devops', 'sre', 'frontend',
    'backend', 'fullstack', 'full-stack', 'mobile', 'ios', 'android',
    'product', 'data', 'ml', 'ai', 'machine learning', 'software',
    'senior', 'staff', 'principal', 'head of', 'vp', 'cto', 'ceo'
  ];

  const rolePattern = new RegExp(
    `((?:${roleKeywords.join('|')})(?:\\s+(?:${roleKeywords.join('|')}))*)`,
    'gi'
  );

  const roles = [];
  let match;
  while ((match = rolePattern.exec(text)) !== null) {
    const role = match[1].trim();
    if (role.length > 3 && !roles.includes(role)) {
      roles.push(role);
    }
  }
  result.roles = roles.slice(0, 3).join(', ');

  const textLower = text.toLowerCase();
  if (textLower.includes('design') || textLower.includes('ux') || textLower.includes('ui')) {
    result.category = 'design';
  } else if (textLower.includes('product manager') || textLower.includes('pm role')) {
    result.category = 'product';
  } else if (textLower.includes('marketing') || textLower.includes('growth')) {
    result.category = 'marketing';
  } else if (textLower.includes('sales') || textLower.includes('account exec')) {
    result.category = 'sales';
  } else if (textLower.includes('operations') || textLower.includes('ops')) {
    result.category = 'ops';
  }

  const reasons = [];
  if (textLower.includes('funded') || textLower.includes('raised') || textLower.includes('series')) {
    reasons.push('Recently funded');
  }
  if (textLower.includes('yc') || textLower.includes('y combinator')) {
    reasons.push('YC-backed');
  }
  if (textLower.includes('remote')) {
    reasons.push('Remote friendly');
  }
  if (textLower.includes('urgent') || textLower.includes('asap') || textLower.includes('immediately')) {
    reasons.push('Urgent hire');
  }
  if (reasons.length === 0) {
    reasons.push('Active hiring post');
  }
  result.why_included = reasons.join(', ');

  return result;
}

async function handleExtract() {
  const url = document.getElementById('captureUrl').value.trim();
  const text = document.getElementById('captureText').value.trim();

  if (!text) {
    showStatus('captureStatus', 'Please paste the post text', 'error');
    return;
  }

  const btn = document.getElementById('extractBtn');
  const btnText = document.getElementById('extractBtnText');
  btn.disabled = true;
  btnText.textContent = 'Extracting...';

  try {
    const extracted = extractInfoFromText(text, url);

    document.getElementById('extractedCompany').value = extracted.company;
    document.getElementById('extractedRoles').value = extracted.roles;
    document.getElementById('extractedCategory').value = extracted.category;
    document.getElementById('extractedWhy').value = extracted.why_included;

    document.getElementById('extractedPreview').style.display = 'block';
    showStatus('captureStatus', 'Info extracted! Review and submit.', 'success');
  } catch (error) {
    showStatus('captureStatus', 'Error extracting: ' + error.message, 'error');
  } finally {
    btn.disabled = false;
    btnText.textContent = 'Extract Info';
  }
}

async function handleSubmitToDb() {
  const url = document.getElementById('captureUrl').value.trim();
  const company = document.getElementById('extractedCompany').value.trim();
  const roles = document.getElementById('extractedRoles').value.trim();
  const category = document.getElementById('extractedCategory').value;
  const why_included = document.getElementById('extractedWhy').value.trim();
  const expiresDays = parseInt(document.getElementById('extractedExpires').value);

  if (!company || !roles) {
    showStatus('captureStatus', 'Company and roles are required', 'error');
    return;
  }

  const btn = document.getElementById('submitToDb');
  btn.disabled = true;
  btn.textContent = 'Submitting...';

  try {
    const expiresAt = new Date();
    expiresAt.setDate(expiresAt.getDate() + expiresDays);

    const payload = {
      tweet_url: url || null,
      company,
      roles,
      category,
      why_included,
      expires_at: expiresAt.toISOString(),
      is_active: true
    };

    const response = await fetch(`${SUPABASE_URL}/rest/v1/hiring_tweets`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'apikey': SUPABASE_KEY,
        'Authorization': `Bearer ${SUPABASE_KEY}`,
        'Prefer': 'return=minimal'
      },
      body: JSON.stringify(payload)
    });

    if (!response.ok) {
      const error = await response.text();
      throw new Error(error);
    }

    showStatus('captureStatus', 'Added to database!', 'success');
    clearCaptureForm();
  } catch (error) {
    showStatus('captureStatus', 'Error: ' + error.message, 'error');
  } finally {
    btn.disabled = false;
    btn.textContent = 'Add to Database';
  }
}

