async function getAnswerForField(field, candidate) {
  // Check cache first
  const cacheKey = generateCacheKey(field, candidate);
  if (settings.enableCache && answerCache[cacheKey]) {
    return answerCache[cacheKey];
  }

  // For basic fields, use direct mapping
  const directAnswer = getDirectAnswer(field, candidate);
  if (directAnswer !== null) {
    return directAnswer;
  }

  // For complex questions, use AI
  if (field.requiresAI) {
    updateQuestionStatus(field.selector, 'generating');

    try {
      const answer = await generateAIAnswer(field, candidate);

      // Cache the answer
      if (settings.enableCache && answer) {
        answerCache[cacheKey] = answer;
        await saveCache();
      }

      return answer;
    } catch (error) {
      console.error('AI generation error:', error);
      return null;
    }
  }

  return null;
}

function getDirectAnswer(field, candidate) {
  const label = (field.label || '').toLowerCase();
  const fieldType = (field.fieldType || 'text').toLowerCase();

  // Name patterns
  if (/first.?name|given.?name|fname/.test(label)) {
    return candidate.name.split(' ')[0] || '';
  }
  if (/last.?name|family.?name|surname|lname/.test(label)) {
    return candidate.name.split(' ').slice(1).join(' ') || '';
  }
  if (/^name$|full.?name|your.?name/.test(label)) {
    return candidate.name;
  }

  // Contact patterns
  if (/e?.?mail/.test(label)) {
    return candidate.email;
  }
  if (/phone.?code|country.?code|dial.?code|calling.?code/.test(label)) {
    // Extract country code from phone if present
    if (candidate.phone && candidate.phone.startsWith('+1')) {
      return '+1';
    }
    return '+1'; // Default to US
  }
  if (/phone|mobile|tel|cell/.test(label) && !/code/.test(label)) {
    // Return phone without country code if it's a separate field
    let phone = candidate.phone || '';
    if (phone.startsWith('+1 ')) phone = phone.substring(3);
    if (phone.startsWith('+1')) phone = phone.substring(2);
    return phone;
  }

  // Links
  if (/linkedin/.test(label)) {
    return candidate.linkedin || '';
  }
  if (/github/.test(label)) {
    return candidate.github || '';
  }
  if (/portfolio|website|personal.?site/.test(label)) {
    return candidate.portfolio || '';
  }

  // Location patterns
  if (/^country$|select.?country|your.?country/.test(label)) {
    // Extract country from location
    const loc = candidate.location || '';
    if (/usa|united states|us$/i.test(loc)) return 'United States';
    const parts = loc.split(',').map(p => p.trim());
    return parts[parts.length - 1] || 'United States';
  }
  if (/^state$|select.?state|province|region/.test(label)) {
    const loc = candidate.location || '';
    const parts = loc.split(',').map(p => p.trim());
    if (parts.length >= 2) return parts[parts.length - 2];
    return '';
  }
  if (/^city$|select.?city/.test(label)) {
    const loc = candidate.location || '';
    const parts = loc.split(',').map(p => p.trim());
    return parts[0] || '';
  }
  if (/location|city|address|where.?are.?you/.test(label) && fieldType !== 'select') {
    return candidate.location || '';
  }

  // Professional
  if (/title|role|position|current.?title|job.?title/.test(label) && fieldType !== 'select') {
    return candidate.title || '';
  }
  if (/company|employer|current.?company|organization/.test(label)) {
    return candidate.company || '';
  }
  if (/years|experience|yoe/.test(label) && fieldType !== 'textarea') {
    return candidate.years || '';
  }
  if (/skills|technologies|tech.?stack/.test(label) && fieldType !== 'textarea') {
    return candidate.skills || '';
  }

  // Work authorization (common in job apps)
  if (/authorized|work.?auth|legally|eligible.?to.?work/.test(label)) {
    // Check candidate's knowledge for work auth info
    if (candidate.knowledge && /us.?citizen|permanent.?resident|green.?card/i.test(candidate.knowledge)) {
      return 'yes';
    }
    return 'yes'; // Default - adjust based on candidate
  }

  // Sponsorship
  if (/sponsor|visa.?sponsor|require.?sponsor/.test(label)) {
    if (candidate.knowledge && /us.?citizen|permanent.?resident/i.test(candidate.knowledge)) {
      return 'no'; // Citizens don't need sponsorship
    }
    return 'no';
  }

  // Remote preference
  if (/remote|on.?site|hybrid|work.?location|work.?preference|work.?arrangement/.test(label)) {
    if (candidate.knowledge && /remote.?only|must.?be.?remote|fully.?remote/i.test(candidate.knowledge)) {
      return 'remote';
    }
    return 'remote';
  }

  // Gender (optional/EEOC)
  if (/gender|sex/.test(label)) {
    return 'Prefer not to say';
  }

  // Ethnicity (optional/EEOC)
  if (/ethnicity|race|ethnic/.test(label)) {
    return 'Prefer not to say';
  }

  // Veteran status (optional/EEOC)
  if (/veteran|military/.test(label)) {
    return 'Prefer not to say';
  }

  // Disability (optional/EEOC)
  if (/disability|disabled/.test(label)) {
    return 'Prefer not to say';
  }

  // How did you hear about us
  if (/hear.?about|how.?did.?you|source|referral/.test(label)) {
    return 'LinkedIn';
  }

  // Willing to relocate
  if (/relocate|relocation/.test(label)) {
    if (candidate.knowledge && /remote.?only/i.test(candidate.knowledge)) {
      return 'no';
    }
    return 'no';
  }

  // Start date / availability
  if (/start.?date|available|when.?can.?you|earliest|notice.?period/.test(label)) {
    if (candidate.knowledge && /immediately|asap|currently.?unemployed/i.test(candidate.knowledge)) {
      return 'Immediately';
    }
    return '2 weeks';
  }

  // Resume/CV (file - can't auto-fill but return indicator)
  if (/resume|cv|curriculum/.test(label) && fieldType === 'file') {
    return '__FILE_UPLOAD__';
  }

  return null;
}

async function generateAIAnswer(field, candidate) {
  const prompt = buildPrompt(field, candidate);

  try {
    const response = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${settings.openaiKey}`
      },
      body: JSON.stringify({
        model: settings.aiModel,
        messages: [
          {
            role: 'system',
            content: `You are helping a job applicant fill out an application form. Generate concise, professional, and compelling answers. Write in first person as the candidate. Be specific and use details from their background when available. Keep answers appropriate for the field type - shorter for single-line inputs, more detailed for text areas.`
          },
          {
            role: 'user',
            content: prompt
          }
        ],
        max_tokens: field.type === 'textarea' ? 500 : 150,
        temperature: 0.7
      })
    });

    if (!response.ok) {
      const error = await response.json();
      throw new Error(error.error?.message || 'API request failed');
    }

    const data = await response.json();
    return data.choices[0]?.message?.content?.trim() || null;

  } catch (error) {
    console.error('OpenAI API error:', error);
    throw error;
  }
}

function buildPrompt(field, candidate) {
  let prompt = `Fill out this job application field:\n\n`;
  prompt += `Field Label: ${field.label || 'Unknown'}\n`;
  prompt += `Field Type: ${field.type}\n`;
  if (field.placeholder) {
    prompt += `Placeholder/Hint: ${field.placeholder}\n`;
  }
  if (field.maxLength) {
    prompt += `Character Limit: ${field.maxLength}\n`;
  }

  prompt += `\nCandidate Information:\n`;
  prompt += `Name: ${candidate.name}\n`;
  prompt += `Email: ${candidate.email}\n`;
  if (candidate.title) prompt += `Current Title: ${candidate.title}\n`;
  if (candidate.company) prompt += `Current Company: ${candidate.company}\n`;
  if (candidate.years) prompt += `Years of Experience: ${candidate.years}\n`;
  if (candidate.skills) prompt += `Skills: ${candidate.skills}\n`;
  if (candidate.location) prompt += `Location: ${candidate.location}\n`;

  if (candidate.knowledge) {
    prompt += `\nDetailed Background Information:\n${candidate.knowledge}\n`;
  }

  if (candidate.summary) {
    prompt += `\nDefault Intro/Summary:\n${candidate.summary}\n`;
  }

  prompt += `\nGenerate an appropriate answer for this field. Do not include any explanations or prefixes - just the answer text itself.`;

  return prompt;
}

function generateCacheKey(field, candidate) {
  const fieldKey = `${field.label || ''}_${field.type || ''}_${field.placeholder || ''}`.toLowerCase().replace(/\s+/g, '_');
  return `${candidate.email}_${fieldKey}`;
}

